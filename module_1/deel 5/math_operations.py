# example:
def increment(nr: float) -> float:
  return nr + 1

def decrement(nr1):
  return nr1 - 1

def add(nr1,nr2):
  return nr1 + nr2

def substract(nr1,nr2):
  return nr1 - nr2

def multiply(nr1,nr2):
  return nr1 * nr2

def divide(nr1,nr2):
  if nr1 == 0 or nr2 == 0:
    return
  else:
    return(nr1 / nr2)

